import esbuild from "esbuild";
import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { format } from "oxfmt";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const appRoot = path.resolve(__dirname, "..");
const repoRoot = path.resolve(appRoot, "../..");
const entry = path.join(appRoot, "src/file-preview/mermaid-webview-entry.ts");
const output = path.join(appRoot, "src/file-preview/mermaid-webview-html.ts");

const result = await esbuild.build({
  entryPoints: [entry],
  bundle: true,
  write: false,
  format: "iife",
  platform: "browser",
  target: ["ios15", "chrome100"],
  minify: true,
  legalComments: "none",
  logLevel: "info",
});

const js = result.outputFiles[0]?.text;
if (!js) {
  throw new Error("Mermaid WebView bundle produced no JavaScript");
}

const html = `<!doctype html>
<html>
  <head>
    <meta charset="utf-8" />
    <meta
      http-equiv="Content-Security-Policy"
      content="default-src 'none'; script-src 'unsafe-inline'; style-src 'unsafe-inline'; img-src data: blob:; font-src data:; connect-src 'none'; frame-src 'none'; object-src 'none'; base-uri 'none'; form-action 'none'"
    />
    <meta
      name="viewport"
      content="width=device-width, initial-scale=1, maximum-scale=1, user-scalable=no, viewport-fit=cover"
    />
  </head>
  <body>
    <script>${js}</script>
  </body>
</html>`;

const contents = `// Generated by packages/app/scripts/build-mermaid-webview-html.mjs.
// Do not edit by hand.

export const mermaidWebViewHtml = ${JSON.stringify(html)};
`;

const formatted = await format(output, contents, {
  printWidth: 100,
  singleQuote: false,
  trailingComma: "all",
  semi: true,
});
if (formatted.errors.length > 0) {
  throw new Error(`Mermaid WebView formatting failed: ${formatted.errors[0]?.message}`);
}

await fs.writeFile(output, formatted.code);
console.log(`Wrote ${path.relative(repoRoot, output)} (${html.length} bytes)`);
